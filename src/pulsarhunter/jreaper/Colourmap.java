    public static Color getFromText(String text){
        if(text.startsWith("#")){
            try{
                int r = Integer.parseInt(text.substring(1,3), 16);
                int g = Integer.parseInt(text.substring(3,5), 16);
                int b = Integer.parseInt(text.substring(5,7), 16);
                return new Color(r,g,b);
            }catch(Exception e){
                return null;
            }
        } else {
            if(text.trim().startsWith("light")) return getFromText(text.trim().substring(5)).brighter();
            if(text.trim().startsWith("dark")) return getFromText(text.trim().substring(4)).darker();
            if(text.trim().equalsIgnoreCase("white"))return Color.white;
            if(text.trim().equalsIgnoreCase("red"))return Color.red;
            if(text.trim().equalsIgnoreCase("green"))return Color.green;
            if(text.trim().equalsIgnoreCase("blue"))return Color.blue;
            if(text.trim().equalsIgnoreCase("black"))return Color.black;
            if(text.trim().equalsIgnoreCase("cyan"))return Color.CYAN;
            if(text.trim().equalsIgnoreCase("gray"))return Color.GRAY;
            if(text.trim().equalsIgnoreCase("magenta"))return Color.magenta;
            if(text.trim().equalsIgnoreCase("orange"))return Color.ORANGE;
            if(text.trim().equalsIgnoreCase("pink"))return Color.pink;
            if(text.trim().equalsIgnoreCase("yellow"))return Color.YELLOW;            
        }
        return null;
    }
    
