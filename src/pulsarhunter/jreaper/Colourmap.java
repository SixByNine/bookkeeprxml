    private Color[] cols;
    private int[][] gCols;
    private boolean isGray;
    private String name;
    /** Creates a new instance of Colourmaps */
    public Colourmap(Color[] cols,String name) {
        isGray = false;
        this.cols = cols;
        this.name = name;
    }
    
    public Colourmap(int[][] cols,String name) {
        isGray = true;
        this.gCols = cols;
        this.name = name;
    }
    
    public String toString(){
        return name;
    }
    
    public int[][] getGCols(){
        return gCols;
    }
    
    public Color[] getCols(){
        return cols;
    }
    
    public boolean equals(Object o){
        if(o instanceof Colourmap){
            return o.toString().equals(this.toString());
        } else return false;
    }
    
    
    
    
